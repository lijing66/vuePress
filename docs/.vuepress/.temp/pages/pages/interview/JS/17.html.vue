<p>什么是高阶高阶函数？</p>
<ul>
<li>高阶函数是一个接收函数作为参数或将函数作为返回值的函数。</li>
<li>例如，Array.prototype.map，Array.prototype.filter 和 Array.prototype.reduce 是语言中内置的一些高阶函数。</li>
<li>闭包的实现与回调函数都是属于高阶函数。</li>
<li>简而言之，高阶函数是一个可以接收函数作为参数，甚至返回一个函数的函数。 它就像常规函数一样，只是多了接收和返回其他函数的附加能力，即参数和输出。</li>
</ul>
